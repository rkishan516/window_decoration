/// Visual effects that can be applied to windows
enum WindowEffect {
  /// Vibrancy/translucent effect (macOS)
  vibrancy,

  /// Window transparency
  transparency,

  /// Blur effect
  blur,

  /// Drop shadow
  shadow,

  /// Acrylic effect (Windows)
  acrylic,

  /// Mica effect (Windows 11)
  mica,
}
